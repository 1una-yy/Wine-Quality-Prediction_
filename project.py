"""### 2. 紅酒數據載入與自定義特徵工程
我們定義了一個 `add_engineered_features` 函式來建立理化指標的比率特徵（如酸度比、酒精密度比等），這些特徵通常能提供比單一指標更強的預測力。接著載入紅酒數據集，進行去重處理並套用特徵工程。
"""

# 定義特徵工程函式並載入紅酒數據
def add_engineered_features(df):
    """建立模型使用的葡萄酒化學特徵比率。"""
    df = df.copy()
    eps = 1e-9
    df = df[df['fixed acidity'] != 0].copy()
    df['acid_ratio'] = df['volatile acidity'] / df['fixed acidity']
    df['total_acidity_calc'] = df['fixed acidity'] + df['volatile acidity'] + df['citric acid']
    df['sulfur_ratio'] = df['free sulfur dioxide'] / (df['total sulfur dioxide'] + eps)
    df['sugar_alcohol_ratio'] = df['residual sugar'] / (df['alcohol'] + eps)
    df['alcohol_density_ratio'] = df['alcohol'] / (df['density'] + eps)
    df['sulphates_chlorides_ratio'] = df['sulphates'] / (df['chlorides'] + eps)
    df['bound_sulfur_dioxide'] = df['total sulfur dioxide'] - df['free sulfur dioxide']
    return df.replace([np.inf, -np.inf], np.nan).dropna()

