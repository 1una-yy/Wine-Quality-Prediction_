def build_multitask_mlp(input_shape, quality_class_count, architecture=(64, 32), dropout_rates=(0.25, 0.15), learning_rate=0.0008):
    """以高品質二元分類為主，輔以原始 quality 等級分類的 MLP。"""
    inputs = layers.Input(shape=(input_shape,))
    x = inputs
    for units, dropout_rate in zip(architecture, dropout_rates):
        x = layers.Dense(units, activation='relu')(x)
        x = layers.BatchNormalization()(x)
        if dropout_rate > 0:
            x = layers.Dropout(dropout_rate)(x)
    high_quality_output = layers.Dense(1, activation='sigmoid')(x)
    quality_output = layers.Dense(quality_class_count, activation='softmax')(x)
    model = Model(inputs, [high_quality_output, quality_output])
    model.compile(
        optimizer=optimizers.Adam(learning_rate=learning_rate),
        loss=['binary_crossentropy', 'sparse_categorical_crossentropy'],
        loss_weights=[1.0, 0.3],
        metrics=[[tf.keras.metrics.AUC(curve='PR', name='pr_auc')], ['accuracy']]
    )
    return model

