def build_optimized_mlp(input_shape, architecture=(128, 64, 32), dropout_rates=(0.35, 0.25, 0.0), learning_rate=0.0005, use_batch_norm=True):
    """建構優化後的 MLP 模型。"""
    model = Sequential([layers.Input(shape=(input_shape,))])
    for units, dropout_rate in zip(architecture, dropout_rates):
        model.add(layers.Dense(units, activation='relu'))
        if use_batch_norm:
            model.add(layers.BatchNormalization())
        if dropout_rate > 0:
            model.add(layers.Dropout(dropout_rate))
    model.add(layers.Dense(1, activation='sigmoid'))
    opt = optimizers.Adam(learning_rate=learning_rate)
    model.compile(
        optimizer=opt,
        loss='binary_crossentropy',
        metrics=['accuracy', tf.keras.metrics.AUC(name='auc'), tf.keras.metrics.AUC(curve='PR', name='pr_auc')]
    )
    return model

