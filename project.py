"""### 4. 模型評估與閾值優化工具函式
為了處理類別不平衡，我們不直接使用預設的 0.5 閾值，而是定義了 `tune_threshold_by_f1` 函式，在驗證集上尋找能極大化 F1-score 的最佳切分點，並以此評估測試集表現。
"""

# 定義模型評估與閾值調整函式
def tune_threshold_by_f1(y_true, y_proba, thresholds=None):
    """尋找最大化 F1-score 的驗證集閾值。"""
    if thresholds is None:
        thresholds = np.linspace(0.05, 0.95, 181)
    scores = [f1_score(y_true, y_proba >= t, zero_division=0) for t in thresholds]
    best_idx = int(np.argmax(scores))
    return float(thresholds[best_idx]), float(scores[best_idx])


def eval_model(model_name, y_true, y_pred, y_proba, threshold=0.5):
    """評估模型效能並繪製圖表。"""
    metrics = {
        '模型': model_name,
        '閾值': threshold,
        '準確率': accuracy_score(y_true, y_pred),
        '精確率': precision_score(y_true, y_pred, zero_division=0),
        '召回率': recall_score(y_true, y_pred, zero_division=0),
        'F1分數': f1_score(y_true, y_pred, zero_division=0),
        'AUC': roc_auc_score(y_true, y_proba),
    }

    print(f"\n========== {model_name} 模型評估 ==========")
    print(f"閾值: {metrics['閾值']:.3f}")
    print(f"準確率 : {metrics['準確率']:.4f}")
    print(f"精確率 : {metrics['精確率']:.4f}")
    print(f"召回率 : {metrics['召回率']:.4f}")
    print(f"F1分數 : {metrics['F1分數']:.4f}")
    print(f"ROC-AUC  : {metrics['AUC']:.4f}")

    plt.figure(figsize=(10, 4))
    cm = confusion_matrix(y_true, y_pred)
    plt.subplot(1, 2, 1)
    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues')
    plt.title(f'{model_name} - 混淆矩陣')
    plt.xlabel('預測值')
    plt.ylabel('實際值')

    fpr, tpr, _ = roc_curve(y_true, y_proba)
    roc_auc = auc(fpr, tpr)
    plt.subplot(1, 2, 2)
    plt.plot(fpr, tpr, label=f'AUC = {roc_auc:.3f}', color='navy')
    plt.plot([0, 1], [0, 1], 'r--', alpha=0.5)
    plt.title(f'{model_name} - ROC 曲線')
    plt.xlabel('假陽性率')
    plt.ylabel('真陽性率')
    plt.legend(loc='lower right')
    plt.tight_layout()
    plt.show()
    return metrics


def evaluate_with_validation_threshold(model_name, y_val, val_proba, y_test, test_proba):
    """使用驗證集調整後的閾值進行評估。"""
    best_threshold, val_f1 = tune_threshold_by_f1(y_val, val_proba)
    print(f"{model_name} 驗證集最佳閾值 = {best_threshold:.3f}, 驗證集 F1 = {val_f1:.4f}")
    y_pred = (test_proba >= best_threshold).astype(int)
    metrics = eval_model(model_name, y_test, y_pred, test_proba, threshold=best_threshold)
    metrics['validation_f1'] = val_f1
    return metrics


def show_logistic_coefficients(model_pipeline, feature_names, title):
    """顯示邏輯回歸模型的特徵係數。"""
    lr = model_pipeline.named_steps['model']
    if 'poly' in model_pipeline.named_steps:
        feature_names = model_pipeline.named_steps['poly'].get_feature_names_out(feature_names)
    coef_df = pd.DataFrame({
        '特徵': feature_names,
        '係數': lr.coef_[0],
        '絕對值係數': np.abs(lr.coef_[0])
    }).sort_values('絕對值係數', ascending=False)
    print(title)
    display(coef_df.head(10))
    return coef_df

